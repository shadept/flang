    // =========================================================================
    // Function forward declarations + definitions
    // =========================================================================

    private static void EmitFunctionForwardDecl(StringBuilder sb, IrFunction fn)
    {
        var retType = fn.UsesReturnSlot ? "void" : IrTypeToCType(fn.ReturnType);
        var name = fn.IsEntryPoint ? "main" : MangleFunctionName(fn);
        if (fn.IsEntryPoint) retType = "int";
        var parms = string.Join(", ", fn.Params.Select(p =>
            p.IsByRef ? EmitVarDecl(new IrPointer(p.Type), p.Name) : EmitVarDecl(p.Type, p.Name)));
        if (string.IsNullOrEmpty(parms)) parms = "void";
        sb.AppendLine($"{retType} {name}({parms});");
    }

    private static void EmitFunction(StringBuilder sb, IrFunction fn, IrModule module)
    {
        var retType = fn.UsesReturnSlot ? "void" : IrTypeToCType(fn.ReturnType);
        var name = fn.IsEntryPoint ? "main" : MangleFunctionName(fn);
        if (fn.IsEntryPoint) retType = "int";
        var parms = string.Join(", ", fn.Params.Select(p =>
            p.IsByRef ? EmitVarDecl(new IrPointer(p.Type), p.Name) : EmitVarDecl(p.Type, p.Name)));
        if (string.IsNullOrEmpty(parms)) parms = "void";
        sb.AppendLine($"{retType} {name}({parms}) {{");

        var lineState = new LineState { Line = -1, FileId = -1 };
        foreach (var block in fn.BasicBlocks)
        {
            sb.AppendLine($"  {block.Label}:;");
            foreach (var inst in block.Instructions)
            {
                EmitLineDirective(sb, inst.Span, module.SourceFiles, ref lineState);
                EmitInstruction(sb, inst, fn.IsEntryPoint);
            }
        }

        if (fn.IsEntryPoint) sb.AppendLine("  return 0;");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    // =========================================================================
    // #line directives
    // =========================================================================

    private static void EmitLineDirective(StringBuilder sb, SourceSpan span, IReadOnlyList<Source> sourceFiles, ref LineState state)
    {
        if (span.FileId < 0 || span.FileId >= sourceFiles.Count) return;

        var source = sourceFiles[span.FileId];
        var line = source.GetLineNumber(span.Index) + 1; // #line is 1-based

        // Only include filename when the file changes
        if (span.FileId != state.FileId)
        {
            var path = source.FileName.Replace("\\", "\\\\");
            sb.AppendLine($"#line {line} \"{path}\"");
        }
        else
        {
            sb.AppendLine($"#line {line}");
        }

        state.Line = line;
        state.FileId = span.FileId;
    }

    // =========================================================================
    // Instruction emission
    // =========================================================================

    private static void EmitInstruction(StringBuilder sb, Instruction inst, bool isEntryPoint = false)
    {
        switch (inst)
        {
            case AllocaInstruction alloca:
                {
                    var resultName = alloca.Result.Name;
                    var irType = alloca.Result.IrType;
                    if (alloca.IsArrayStorage && irType is IrPointer { Pointee: IrArray arr })
                    {
                        // Array data alloca: emit actual C array for the storage
                        var elemC = IrTypeToCType(arr.Element);
                        var length = Math.Max(arr.Length ?? 0, 1); // minimum 1 for MSVC
                        sb.AppendLine($"    {elemC} _store_{resultName}[{length}];");
                        sb.AppendLine($"    {elemC}* {resultName} = _store_{resultName};");
                    }
                    else if (irType is IrPointer ptr)
                    {
                        sb.AppendLine($"    {EmitVarDecl(ptr.Pointee, $"_store_{resultName}")};");
                        sb.AppendLine($"    {EmitVarDecl(irType, resultName)} = &_store_{resultName};");
                    }
                    else
                    {
                        sb.AppendLine($"    uint8_t _store_{resultName}[{alloca.SizeInBytes}];");
                        sb.AppendLine($"    void* {resultName} = _store_{resultName};");
                    }
                    break;
                }

            case StorePointerInstruction store:
                {
                    var ptr = EmitValue(store.Pointer);
                    var val = EmitValue(store.Value);
                    var pointeeType = store.Pointer.IrType is IrPointer p ? p.Pointee : null;
                    if (pointeeType != null)
                    {
                        // Use IrPointer wrapper to handle function pointer syntax correctly
                        var castType = IrTypeToCType(new IrPointer(pointeeType));
                        sb.AppendLine($"    *({castType}){ptr} = {val};");
                    }
                    else
                        sb.AppendLine($"    *{ptr} = {val};");
                    break;
                }

            case LoadInstruction load:
                {
                    var resultName = load.Result.Name;
                    var resultIrType = load.Result.IrType ?? TypeLayoutService.IrI32;
                    var ptr = EmitValue(load.Pointer);
                    var castType = IrTypeToCType(new IrPointer(resultIrType));
                    sb.AppendLine($"    {EmitVarDecl(resultIrType, resultName)} = *({castType}){ptr};");
                    break;
                }

            case ReturnInstruction ret:
                {
                    if (ret.Value.IrType == TypeLayoutService.IrVoidPrim ||
                        ret.Value.IrType == null)
                        sb.AppendLine(isEntryPoint ? "    return 0;" : "    return;");
                    else
                        sb.AppendLine($"    return {EmitValue(ret.Value)};");
                    break;
                }

            case BinaryInstruction bin:
                {
                    var resultName = bin.Result.Name;
                    var resultType = IrTypeToCType(bin.Result.IrType ?? TypeLayoutService.IrI32);
                    var left = EmitValue(bin.Left);
                    var right = EmitValue(bin.Right);
                    if (bin.Operation == BinaryOp.UnsignedShiftRight)
                    {
                        // >>> (logical shift): cast to unsigned, shift, cast back
                        var leftIr = bin.Left.IrType ?? TypeLayoutService.IrI32;
                        var unsignedType = leftIr == TypeLayoutService.IrI32 ? "uint32_t"
                            : leftIr == TypeLayoutService.IrI64 || leftIr == TypeLayoutService.IrISize ? "uint64_t"
                            : leftIr == TypeLayoutService.IrI16 ? "uint16_t"
                            : leftIr == TypeLayoutService.IrI8 ? "uint8_t"
                            : IrTypeToCType(leftIr); // already unsigned
                        sb.AppendLine($"    {resultType} {resultName} = ({resultType})(({unsignedType}){left} >> {right});");
                    }
                    else if (bin.Operation == BinaryOp.Modulo && IsFloatIrType(bin.Left.IrType))
                    {
                        sb.AppendLine($"    {resultType} {resultName} = fmod({left}, {right});");
                    }
                    else
                    {
                        var op = BinaryOpToC(bin.Operation);
                        sb.AppendLine($"    {resultType} {resultName} = {left} {op} {right};");
                    }
                    break;
                }

            case UnaryInstruction unary:
                {
                    var resultName = unary.Result.Name;
                    var resultType = IrTypeToCType(unary.Result.IrType ?? TypeLayoutService.IrI32);
                    var operand = EmitValue(unary.Operand);
                    var op = unary.Operation == UnaryOp.Negate ? "-" : "!";
                    sb.AppendLine($"    {resultType} {resultName} = {op}{operand};");
                    break;
                }

            case CallInstruction call:
                {
                    var resultName = call.Result.Name;
                    var resultType = call.Result.IrType ?? TypeLayoutService.IrVoidPrim;
                    var args = string.Join(", ", call.Arguments.Select(a => EmitArg(a, call.IsForeignCall)));
                    var fnName = call.IsForeignCall
                        ? call.FunctionName
                        : IrNameMangling.MangleFunctionName(call.FunctionName,
                            call.CalleeIrParamTypes
                            ?? (IReadOnlyList<IrType>)call.Arguments.Select(a => a.IrType ?? TypeLayoutService.IrI32).ToArray());

                    if (resultType == TypeLayoutService.IrVoidPrim)
                        sb.AppendLine($"    {fnName}({args});");
                    else
                        sb.AppendLine($"    {EmitVarDecl(resultType, resultName)} = {fnName}({args});");
                    break;
                }

            case IndirectCallInstruction indirectCall:
                {
                    var funcPtrExpr = EmitValue(indirectCall.FunctionPointer);
                    var args = string.Join(", ", indirectCall.Arguments.Select(EmitValue));
                    var resultName = indirectCall.Result.Name;
                    var resultType = indirectCall.Result.IrType ?? TypeLayoutService.IrVoidPrim;

                    if (resultType == TypeLayoutService.IrVoidPrim)
                        sb.AppendLine($"    {funcPtrExpr}({args});");
                    else
                        sb.AppendLine($"    {EmitVarDecl(resultType, resultName)} = {funcPtrExpr}({args});");
                    break;
                }

            case CastInstruction cast:
                {
                    var resultName = cast.Result.Name;
                    var resultIrType = cast.Result.IrType ?? TypeLayoutService.IrI32;
                    var resultType = IrTypeToCType(resultIrType);
                    var src = EmitValue(cast.Source);
                    sb.AppendLine($"    {EmitVarDecl(resultIrType, resultName)} = ({resultType}){src};");
                    break;
                }

            case GetElementPtrInstruction gep:
                {
                    var resultName = gep.Result.Name;
                    var resultIrType = gep.Result.IrType ?? TypeLayoutService.IrVoidPrim;
                    var resultType = IrTypeToCType(resultIrType);
                    var basePtr = EmitValue(gep.BasePointer);
                    var offset = EmitValue(gep.ByteOffset);
                    sb.AppendLine($"    {EmitVarDecl(resultIrType, resultName)} = ({resultType})((uint8_t*){basePtr} + {offset});");
                    break;
                }

            case BranchInstruction branch:
                {
                    var cond = EmitValue(branch.Condition);
                    sb.AppendLine($"    if ({cond}) goto {branch.TrueBlock.Label};");
                    sb.AppendLine($"    goto {branch.FalseBlock.Label};");
                    break;
                }

            case JumpInstruction jump:
                sb.AppendLine($"    goto {jump.TargetBlock.Label};");
                break;

            case AddressOfInstruction addrOf:
                {
                    var resultName = addrOf.Result.Name;
                    var resultType = IrTypeToCType(addrOf.Result.IrType ?? TypeLayoutService.IrVoidPrim);
                    sb.AppendLine($"    {resultType} {resultName} = &{addrOf.VariableName};");
                    break;
                }

            case CopyInstruction copy:
                {
                    var src = EmitValue(copy.SrcPtr);
                    var dst = EmitValue(copy.DstPtr);
                    var castType = IrTypeToCType(new IrPointer(copy.ValueType));
                    sb.AppendLine($"    *({castType}){dst} = *({castType}){src};");
                    break;
                }

            case CopyFromOffsetInstruction cfo:
                {
                    var resultName = cfo.Result.Name;
                    var resultIrType = cfo.Result.IrType ?? TypeLayoutService.IrI32;
                    var src = EmitValue(cfo.SrcPtr);
                    var offset = EmitValue(cfo.ByteOffset);
                    var castType = IrTypeToCType(new IrPointer(resultIrType));
                    sb.AppendLine($"    {EmitVarDecl(resultIrType, resultName)} = *({castType})((uint8_t*){src} + {offset});");
                    break;
                }

            case CopyToOffsetInstruction cto:
                {
                    var val = EmitValue(cto.Val);
                    var dst = EmitValue(cto.DstPtr);
                    var offset = EmitValue(cto.ByteOffset);
                    var castType = IrTypeToCType(new IrPointer(cto.ValueType));
                    sb.AppendLine($"    *({castType})((uint8_t*){dst} + {offset}) = {val};");
                    break;
                }

            default:
                sb.AppendLine($"    /* TODO: {inst.GetType().Name} */");
                break;
        }
    }

    // =========================================================================
    // Value emission
    // =========================================================================

    private static string EmitValue(Value value)
    {
        return value switch
        {
            IntConstantValue cv => EmitConstant(cv),
            FloatConstantValue fcv => EmitFloatConstant(fcv),
            LocalValue lv => lv.Name,
            StringTableValue stv => $"__flang__string_table[{stv.Index}]",
            FunctionReferenceValue fv => fv.IrType is IrFunctionPtr fp
                ? IrNameMangling.MangleFunctionName(fv.Name, fp.Params)
                : fv.Name,
            RawCStringValue rv => $"\"{rv.Text}\"",
            GlobalValue gv when gv.Initializer is StructConstantValue
                => $"&{gv.Name}",
            GlobalValue gv => gv.Name,
            _ => $"/* unknown value: {value.GetType().Name} */"
        };
    }

    /// <summary>
    /// Emit an integer constant with the appropriate C suffix for its type.
    /// </summary>
    private static string EmitConstant(IntConstantValue cv)
    {
        // INT64_MIN can't be written as -9223372036854775808LL in C because the positive
        // part overflows signed long long. Use the (-MAX - 1) idiom instead.
        if (cv.IntValue == long.MinValue && cv.IrType is IrPrimitive p64
            && (p64 == TypeLayoutService.IrI64 || p64 == TypeLayoutService.IrISize))
            return "(-9223372036854775807LL - 1LL)";

        var lit = cv.IntValue.ToString();
        if (cv.IrType is IrPrimitive p)
        {
            if (p == TypeLayoutService.IrU64 || p == TypeLayoutService.IrUSize)
                return lit + "ULL";
            if (p == TypeLayoutService.IrI64 || p == TypeLayoutService.IrISize)
                return lit + "LL";
            if (p == TypeLayoutService.IrU32)
                return lit + "U";
        }
        return lit;
    }

    /// <summary>
    /// Emit a floating-point constant with the appropriate C suffix.
    /// </summary>
    private static string EmitFloatConstant(FloatConstantValue fcv)
    {
        if (double.IsNaN(fcv.FloatValue))
            return "NAN";
        if (double.IsPositiveInfinity(fcv.FloatValue))
            return "INFINITY";
        if (double.IsNegativeInfinity(fcv.FloatValue))
            return "(-INFINITY)";

        // Use R format for round-trip precision
        var lit = fcv.FloatValue.ToString("R", System.Globalization.CultureInfo.InvariantCulture);
        // Ensure the literal always has a decimal point so C sees it as floating-point
        if (!lit.Contains('.') && !lit.Contains('E') && !lit.Contains('e'))
            lit += ".0";

        if (fcv.IrType == TypeLayoutService.IrF32)
            return lit + "f";
        return lit;
    }

    /// <summary>
    /// Emit a value as a call argument. For foreign calls, casts uint8_t* to (char*)
    /// to avoid clang -Wpointer-sign warnings (FLang uses u8 for bytes, C uses char).
    /// Uses (char*) not (const char*) so it's compatible with both const and non-const params.
    /// </summary>
    private static string EmitArg(Value value, bool isForeignCall)
    {
        var emitted = EmitValue(value);
        if (isForeignCall && value.IrType is IrPointer { Pointee: IrPrimitive p } && p == TypeLayoutService.IrU8)
            return $"(char*){emitted}";
        return emitted;
    }

    // =========================================================================
    // Type mapping
    // =========================================================================

    /// <summary>
    /// Transform an IrFunctionPtr to its ABI form: large value params become pointers,
    /// large return type becomes void with a hidden __ret pointer param.
    /// </summary>
    private static (IrType[] Params, IrType Return) GetAbiFunctionPtr(IrFunctionPtr fp)
    {
        var abiParams = new List<IrType>();
        var usesRetSlot = TypeLayoutService.IsLargeValue(fp.Return);
        if (usesRetSlot)
            abiParams.Add(new IrPointer(fp.Return));
        foreach (var p in fp.Params)
            abiParams.Add(TypeLayoutService.IsLargeValue(p) ? new IrPointer(p) : p);
        return ([.. abiParams], usesRetSlot ? TypeLayoutService.IrVoidPrim : fp.Return);
    }

    /// <summary>
    /// Emit a C field/variable declaration. Handles function pointer syntax:
    /// C requires `ret (*name)(params)` not `ret(*)(params) name`.
    /// </summary>
    private static string EmitFieldDecl(IrType type, string name)
    {
        if (type is IrFunctionPtr fp)
        {
            var (abiParams, abiRet) = GetAbiFunctionPtr(fp);
            var ret = IrTypeToCType(abiRet);
            var parms = string.Join(", ", abiParams.Select(IrTypeToCType));
            return $"{ret} (*{name})({parms})";
        }
        return $"{IrTypeToCType(type)} {name}";
    }

    private static string IrTypeToCType(IrType? type)
    {
        return type switch
        {
            null => "void",
            IrPrimitive p => p.Name switch
            {
                "void" => "void",
                "never" => "void",
                "bool" => "bool",
                "i8" => "int8_t",
                "i16" => "int16_t",
                "i32" => "int32_t",
                "i64" => "int64_t",
                "u8" => "uint8_t",
                "u16" => "uint16_t",
                "u32" => "uint32_t",
                "u64" => "uint64_t",
                "isize" => "int64_t",
                "usize" => "uint64_t",
                "char" => "uint32_t",
                "f32" => "float",
                "f64" => "double",
                _ => "int32_t"
            },
            IrPointer ptr when ptr.Pointee is IrFunctionPtr fp2 =>
                EmitFuncPtrType(fp2, "**"),
            IrPointer ptr => $"{IrTypeToCType(ptr.Pointee)}*",
            IrArray arr => $"{IrTypeToCType(arr.Element)}*",
            IrStruct s => s.CName,
            IrEnum e => e.CName,
            IrFunctionPtr fp => EmitFuncPtrType(fp, "*"),
            _ => "void"
        };
    }

    private static string EmitFuncPtrType(IrFunctionPtr fp, string indirection)
    {
        var (abiParams, abiRet) = GetAbiFunctionPtr(fp);
        return $"{IrTypeToCType(abiRet)}({indirection})({string.Join(", ", abiParams.Select(IrTypeToCType))})";
    }

    private static string BinaryOpToC(BinaryOp op)
    {
        return op switch
        {
            BinaryOp.Add => "+",
            BinaryOp.Subtract => "-",
            BinaryOp.Multiply => "*",
            BinaryOp.Divide => "/",
            BinaryOp.Modulo => "%",
            BinaryOp.Equal => "==",
            BinaryOp.NotEqual => "!=",
            BinaryOp.LessThan => "<",
            BinaryOp.GreaterThan => ">",
            BinaryOp.LessThanOrEqual => "<=",
            BinaryOp.GreaterThanOrEqual => ">=",
            BinaryOp.BitwiseAnd => "&",
            BinaryOp.BitwiseOr => "|",
            BinaryOp.BitwiseXor => "^",
            BinaryOp.ShiftLeft => "<<",
            BinaryOp.ShiftRight => ">>",
            _ => "/* unknown op */"
        };
    }

    private static bool IsFloatIrType(IrType? type) =>
        type == TypeLayoutService.IrF32 || type == TypeLayoutService.IrF64;

    // =========================================================================
    // Helpers
    // =========================================================================

    private static string MangleFunctionName(IrFunction fn)
    {
        var paramList = fn.UsesReturnSlot ? fn.Params.Skip(1) : fn.Params;
        return IrNameMangling.MangleFunctionName(fn.Name, [.. paramList.Select(p => p.Type)]);
    }

    /// <summary>
    /// Emit a C variable/parameter declaration. Function pointer types and pointers
    /// to function pointers need the name embedded: ret(*name)(params), ret(**name)(params).
    /// </summary>
    private static string EmitVarDecl(IrType type, string name)
    {
        if (type is IrFunctionPtr fp)
        {
            var (abiParams, abiRet) = GetAbiFunctionPtr(fp);
            var ret = IrTypeToCType(abiRet);
            var parms = string.Join(", ", abiParams.Select(IrTypeToCType));
            return $"{ret}(*{name})({parms})";
        }
        if (type is IrPointer { Pointee: IrFunctionPtr fp2 })
        {
            var (abiParams, abiRet) = GetAbiFunctionPtr(fp2);
            var ret = IrTypeToCType(abiRet);
            var parms = string.Join(", ", abiParams.Select(IrTypeToCType));
            return $"{ret}(**{name})({parms})";
        }
        return $"{IrTypeToCType(type)} {name}";
    }

    private static string EscapeString(string s)
    {
        return new StringBuilder(s)
            .Replace("\\", "\\\\")
            .Replace("\"", "\\\"")
            .Replace("\n", "\\n")
            .Replace("\r", "\\r")
            .Replace("\t", "\\t")
            .Replace("\0", "\\0")
            .ToString();
    }
