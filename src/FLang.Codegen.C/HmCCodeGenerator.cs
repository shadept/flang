    // =========================================================================
    // Global constants
    // =========================================================================

    private static void EmitGlobalForwardDecl(StringBuilder sb, GlobalValue gv)
    {
        if (gv.Initializer is StructConstantValue scv && scv.IrType != null)
            sb.AppendLine($"static {IrTypeToCType(scv.IrType)} {gv.Name};");
        else if (gv.Initializer is ArrayConstantValue acv && acv.Elements != null && acv.IrType is IrArray arrTy)
            sb.AppendLine($"static {IrTypeToCType(arrTy.Element)} {gv.Name}[{acv.Elements.Length}];");
    }

    private static void EmitGlobalValue(StringBuilder sb, GlobalValue gv, List<StringTableEntry> stringTable)
    {
        if (gv.Initializer is StructConstantValue scv)
        {
            var irType = scv.IrType;
            if (irType == null) return;

            sb.Append($"static {IrTypeToCType(irType)} {gv.Name} = {{ ");
            if (irType is IrStruct irStruct)
            {
                var parts = new List<string>();
                foreach (var field in irStruct.Fields)
                {
                    if (scv.FieldValues.TryGetValue(field.Name, out var fv))
                        parts.Add($".{field.Name} = {EmitGlobalFieldValue(fv, field.Type, stringTable)}");
                    else
                        parts.Add($".{field.Name} = {{0}}");
                }
                sb.Append(string.Join(", ", parts));
            }
            else if (irType is IrEnum irEnum)
            {
                sb.Append(EmitEnumConstantBody(scv, irEnum));
            }
            sb.AppendLine(" };");
        }
        else if (gv.Initializer is ArrayConstantValue acv && acv.Elements != null && acv.IrType is IrArray arrTy)
        {
            var elemType = arrTy.Element;
            sb.Append($"static {IrTypeToCType(elemType)} {gv.Name}[{acv.Elements.Length}] = {{ ");
            var parts = new List<string>();
            foreach (var elem in acv.Elements)
            {
                if (elem is StructConstantValue elemScv && elemScv.IrType is IrStruct elemStruct)
                    parts.Add(EmitNestedStructConstant(elemScv, elemStruct, stringTable));
                else
                    parts.Add("{0}");
            }
            sb.Append(string.Join(", ", parts));
            sb.AppendLine(" };");
        }
    }

    private static string EmitGlobalFieldValue(Value value, IrType targetType, List<StringTableEntry> stringTable)
    {
        return value switch
        {
            IntConstantValue cv => EmitConstant(cv),
            FloatConstantValue fcv => EmitFloatConstant(fcv),
            StringTableValue stv => EmitInlineStringLiteral(stringTable[stv.Index]),
            FunctionReferenceValue fv => fv.IrType is IrFunctionPtr fp2
                ? IrNameMangling.MangleFunctionName(fv.Name, fp2.Params)
                : fv.Name,
            StructConstantValue nested when nested.IrType is IrStruct nestedStruct
                => EmitNestedStructConstant(nested, nestedStruct, stringTable),
            StructConstantValue nested when nested.IrType is IrEnum nestedEnum
                => $"{{ {EmitEnumConstantBody(nested, nestedEnum)} }}",
            ArrayConstantValue arr when arr.Data != null
                => $"(uint8_t*)\"{EscapeString(arr.StringRepresentation ?? "")}\"",
            GlobalValue gv when gv.Initializer is ArrayConstantValue
                => gv.Name,
            GlobalValue gv when gv.Initializer is StructConstantValue
                => targetType is IrPointer
                    ? $"({IrTypeToCType(targetType)})&{gv.Name}"
                    : $"&{gv.Name}",
            GlobalValue gv => $"&{gv.Name}",
            _ => "{0}"
        };
    }

    /// <summary>Inline a string table entry as a C struct literal { (uint8_t*)"...", len }.</summary>
    private static string EmitInlineStringLiteral(StringTableEntry entry)
    {
        var escaped = EscapeString(entry.Value);
        var len = entry.Utf8Data.Length - 1; // exclude null terminator
        return $"{{ (uint8_t*)\"{escaped}\", {len} }}";
    }

    private static string EmitNestedStructConstant(StructConstantValue scv, IrStruct irStruct, List<StringTableEntry> stringTable)
    {
        var parts = new List<string>();
        foreach (var field in irStruct.Fields)
        {
            if (scv.FieldValues.TryGetValue(field.Name, out var fv))
                parts.Add($".{field.Name} = {EmitGlobalFieldValue(fv, field.Type, stringTable)}");
            else
                parts.Add($".{field.Name} = {{0}}");
        }
        return $"{{ {string.Join(", ", parts)} }}";
    }

    private static string EmitEnumConstantBody(StructConstantValue scv, IrEnum irEnum)
    {
        // Enum in C is: struct { int32_t tag; uint8_t payload[N]; }
        // For naked variants (no payload), just set the tag
        if (scv.FieldValues.TryGetValue("tag", out var tagVal) && tagVal is IntConstantValue cv)
            return $".tag = {EmitConstant(cv)}";
        return ".tag = 0";
    }

    private static void EmitStringTable(StringBuilder sb, IrModule module)
    {
        if (module.StringTable.Count == 0) return;

        sb.AppendLine($"static const core_string_String __flang__string_table[{module.StringTable.Count}] = {{");
        for (var i = 0; i < module.StringTable.Count; i++)
        {
            var entry = module.StringTable[i];
            var escaped = EscapeString(entry.Value);
            var comma = i < module.StringTable.Count - 1 ? "," : "";
            sb.AppendLine($"    {{ (uint8_t*)\"{escaped}\", {entry.Utf8Data.Length - 1} }}{comma}");
        }
        sb.AppendLine("};");
        sb.AppendLine();
    }

